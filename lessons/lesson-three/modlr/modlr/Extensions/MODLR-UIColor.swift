import UIKit

extension UIColor {
    
    // base yellow for all the things
    class func lightYellow() -> UIColor {
        return UIColor(red:1,  green:0.965,  blue:0.353, alpha:1)
    }
    
    // secondary, slightly darker yellow shade
    class func medYellow() -> UIColor {
        return UIColor(red:1,  green:0.910,  blue:0.353, alpha:1)
    }
}